CREATE TABLE Product (
         id SERIAL,
         product_name VARCHAR(255) NOT NULL,
         description TEXT,
         price DECIMAL(10, 2) NOT NULL,
         stock_quantity INT NOT NULL,
         category VARCHAR(100),
         manufacturer VARCHAR(100),
         weight DECIMAL(5, 2),
         dimensions VARCHAR(50),
         color VARCHAR(50),
         material VARCHAR(50),
         sku VARCHAR(50),
         barcode VARCHAR(50),
         supplier_id BIGINT,
         brand VARCHAR(100),
         release_date DATE,
         expiration_date DATE,
         created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
         updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
         discontinued BOOLEAN DEFAULT FALSE,
         warranty_period INT,
         returnable BOOLEAN DEFAULT TRUE,
         recyclable BOOLEAN DEFAULT TRUE,
         energy_rating VARCHAR(10),
         country_of_origin VARCHAR(100),
         shipping_weight DECIMAL(5, 2),
         package_dimensions VARCHAR(50),
         max_temperature DECIMAL(5, 2),
         min_temperature DECIMAL(5, 2),
         certifications VARCHAR(255),
         hazardous_material BOOLEAN DEFAULT FALSE,
         battery_life_hours DECIMAL(5, 2),
         power_source VARCHAR(50),
         voltage DECIMAL(5, 2),
         amperage DECIMAL(5, 2),
         frequency DECIMAL(5, 2),
         connectivity VARCHAR(100),
         wireless BOOLEAN DEFAULT FALSE,
         bluetooth BOOLEAN DEFAULT FALSE,
         warranty_terms TEXT,
         care_instructions TEXT,
         cleaning_instructions TEXT,
         packaging_type VARCHAR(50),
         recyclable_packaging BOOLEAN DEFAULT TRUE,
         water_resistant BOOLEAN DEFAULT FALSE,
         waterproof BOOLEAN DEFAULT FALSE,
         shock_resistant BOOLEAN DEFAULT FALSE,
         assembly_required BOOLEAN DEFAULT FALSE,
         assembly_instructions TEXT,
         user_manual TEXT,
         safety_instructions TEXT,
         age_restriction INT,
         customer_rating DECIMAL(3, 2),
         number_of_reviews INT,
         average_review_score DECIMAL(3, 2),
         product_url VARCHAR(255),
         video_url VARCHAR(255),
         image_url VARCHAR(255),
         thumbnail_url VARCHAR(255),
         promotional_text TEXT,
         discount_percentage DECIMAL(5, 2),
         sale_start_date DATE,
         sale_end_date DATE,
         max_order_quantity INT,
         min_order_quantity INT,
         lead_time_days INT,
         backorder_allowed BOOLEAN DEFAULT FALSE,
         restock_date DATE,
         restock_quantity INT,
         product_weight DECIMAL(5, 2),
         shipping_class VARCHAR(50),
         fulfillment_center VARCHAR(100),
         product_condition VARCHAR(50),
         refurbished BOOLEAN DEFAULT FALSE,
         used BOOLEAN DEFAULT FALSE,
         limited_edition BOOLEAN DEFAULT FALSE,
         gift_wrap_available BOOLEAN DEFAULT FALSE,
         gift_message_available BOOLEAN DEFAULT FALSE,
         subscription_available BOOLEAN DEFAULT FALSE,
         subscription_interval INT,
         subscription_price DECIMAL(10, 2),
         customizability_options TEXT,
         seasonal BOOLEAN DEFAULT FALSE,
         eco_friendly BOOLEAN DEFAULT FALSE,
         carbon_footprint DECIMAL(5, 2),
         time_to_assemble_minutes INT,
         digital_download BOOLEAN DEFAULT FALSE,
         license_key VARCHAR(100),
         operating_systems_supported TEXT,
         compatible_with TEXT,
         languages_supported TEXT,
         update_frequency VARCHAR(50),
         available_languages TEXT,
         digital_rights_management VARCHAR(100),
         upc_code VARCHAR(50),
         ean_code VARCHAR(50),
         mpn_code VARCHAR(50),
         isbn_code VARCHAR(50),
         model_number VARCHAR(50)
);
